def analyse_shellcode(name: str, shellcode: bytes) -> None:
    """
    Run all analysis steps on a shellcode and print results.
    """
    print(f"\n{'='*60}")
    print(f"  {name}  ({len(shellcode)} bytes)")
    print(f"{'='*60}")

    # Step 1 — Strings extraction (comme 'echo -ne $shellcode | strings')
    print("\n[1] Extracted strings:")
    strings = get_shellcode_strings(shellcode)
    print(strings)

    # Step 2 — Capstone disassembly
    print("\n[2] Capstone disassembly (x86 32-bit):")
    disasm = get_capstone_analysis(shellcode)
    print(disasm)

    # Step 3 — Pylibemu emulation
    print("\n[3] Pylibemu emulation:")
    emu = get_pylibemu_analysis(shellcode)
    print(emu)


